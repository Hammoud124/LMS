    public AddNewBookFrame(@Nullable Book book) {
        initComponents();
        this.setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);

        viewModel.init(book);
        if (book != null) {
            showBookDetails(book);
            this.book = book;
        }

        addbook.setText(book != null ? "Edit" : "Add");

        // Enable the button if we're in edit mode.
        addbook.setEnabled(book != null);


        viewModel.addOnAddEditButtonEnabledListener(evt -> {
            addbook.setEnabled((boolean) evt.getNewValue());
        });

        bookName.getDocument().addDocumentListener(new DefaultDocumentListener(e -> {
            viewModel.setTitle(bookName.getText());
        }));

        author.getDocument().addDocumentListener(new DefaultDocumentListener(e -> {
            viewModel.setAuthor(author.getText());
        }));

        serialNum.getDocument().addDocumentListener(new DefaultDocumentListener(e -> {
            viewModel.setISBN(serialNum.getText());
        }));

        publishYear.getDocument().addDocumentListener(new DefaultDocumentListener(e -> {
            viewModel.setPublicationDate(publishYear.getText());
        }));

        category.getDocument().addDocumentListener(new DefaultDocumentListener(e -> {
            viewModel.setGenre(category.getText());
        }));

        page.getDocument().addDocumentListener(new DefaultDocumentListener(e -> {
            try {
                viewModel.setPages(Integer.parseInt(page.getText()));
            } catch (NumberFormatException nfe) {
            }
        }));

        details.getDocument().addDocumentListener(new DefaultDocumentListener(e -> {
            viewModel.setAdditionalDetails(details.getText());
        }));

    }

    private void showBookDetails(Book book) {
        bookName.setText(book.getTitle());
        author.setText(book.getAuthor());
        serialNum.setText(book.getISBN());
        category.setText(book.getGenre().getName()  );
        publishYear.setText((book.getFormattedPublicationDate()));
        details.setText(book.getAdditionalDetails());
        page.setText(String.valueOf(book.getPages()));
        try {
            BufferedImage bufferedImage = ImageIO.read(new File(book.getImagePathOrDefault()));
            Image icon = bufferedImage.getScaledInstance(48, 48, Image.SCALE_SMOOTH);
            picBox.setIcon(new ImageIcon(icon));
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Image not found");
        }
    }
