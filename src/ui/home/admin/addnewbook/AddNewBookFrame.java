    private void chooseImageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chooseImageActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Choose image");
        fileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int returnValue = fileChooser.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();

            if (selectedFile.getName().toLowerCase().endsWith(".jpg")
                    || selectedFile.getName().toLowerCase().endsWith(".png")
                    || selectedFile.getName().toLowerCase().endsWith(".jpeg")) {

                ImageIcon imageIcon = new ImageIcon(selectedFile.getAbsolutePath());

                Image image = imageIcon.getImage();
                Image resizedImage = image.getScaledInstance(picBox.getWidth(), picBox.getHeight(), java.awt.Image.SCALE_SMOOTH);
                imageIcon = new ImageIcon(resizedImage);

                picBox.setIcon(imageIcon);
            }
        }
    }//GEN-LAST:event_chooseImageActionPerformed

